// Display names for each time of day
const TimeLabels = {
  morning: 'Morning',
  afternoon: 'Afternoon',
  evening: 'Evening',
  night: 'Night',
};

/**
 * TimeIndicator component that shows the current time of day
 * with an appropriate icon and applies time-based styling
 */
export function TimeIndicator() {
  const theme = useTimeTheme();
  const TimeIcon = TimeIcons[theme.timeOfDay];

  return (
    <div className="time-indicator">
      <TimeIcon />
      <span>{TimeLabels[theme.timeOfDay]}</span>
    </div>
  );
}

export default TimeIndicator;